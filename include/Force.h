#ifndef FORCE_H
#define FORCE_H

#include <vector>
#include <ngl/Vec3.h>
#include "Flock.h"

class Force
{
  public:
    //----------------------------------------------------------------------------------------------------------------------
    /// @brief ctor initializes the vectors of the class
    //----------------------------------------------------------------------------------------------------------------------
    Force();
    //----------------------------------------------------------------------------------------------------------------------
    /// @brief dtor must release memory used for the spring nodes
    //----------------------------------------------------------------------------------------------------------------------
    ~Force(){}
    //----------------------------------------------------------------------------------------------------------------------
    /// @brief function to add all the forces
    /// @param[in] m_cloth the cloth network
    //----------------------------------------------------------------------------------------------------------------------
    void addForces(Flock *m_flock);
    //----------------------------------------------------------------------------------------------------------------------
    /// @brief function to reset anchor positions
    /// @param[in] m_node the cloth node
    /// @param[out] the spring damping force
    //----------------------------------------------------------------------------------------------------------------------
    ngl::Vec3 addDampingForce(Node* m_node);
};

#endif // FORCE_H
